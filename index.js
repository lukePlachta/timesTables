function oneTimesTable() {
    var display = "";
    for (i = 1; i < 13; i++) {
        var multiplier = 1;
        var result = i * 1;
    display += multiplier + " * " + i + " = " + result + "<br>";    
    }
    document.getElementById("outputDivOne").innerHTML = display;
}
